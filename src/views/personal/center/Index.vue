            <p>
              <i class="address" />
              <span>浙江省</span>
              <span>杭州市</span>
            </p>
          </div>
          <a-divider />

          <div class="personal-center-tags">
            <div class="tagsTitle">
              标签
            </div>
            <div>
              <template v-for="(tag, index) in tags">
                <a-tooltip v-if="tag.length > 20" :key="tag" :title="tag">
                  <a-tag :key="tag" :closable="index !== 0" :after-close="() => handleTagClose(tag)">
                    {{ `${tag.slice(0, 20)}...` }}
                  </a-tag>
                </a-tooltip>
                <a-tag v-else :key="tag" :closable="index !== 0" :after-close="() => handleTagClose(tag)">
                  {{ tag }}
                </a-tag>
              </template>
              <a-input
                v-if="tagInputVisible"
                ref="tagInput"
                type="text"
                size="small"
                :style="{ width: '78px' }"
                :value="tagInputValue"
                @change="handleInputChange"
                @blur="handleTagInputConfirm"
                @keyup.enter="handleTagInputConfirm"
              />
              <a-tag v-else @click="showTagInput" style="background: #fff; borderStyle: dashed;">
                <a-icon type="plus" />New Tag
              </a-tag>
            </div>
          </div>
          <a-divider :dashed="true" />

          <div class="personal-center-team">
            <div class="teamTitle">
              团队
            </div>
            <a-spin :spinning="teamSpinning">
              <div class="members">
                <a-row>
                  <a-col :span="12" v-for="(item, index) in teams" :key="index">
                    <a>
                      <a-avatar size="small" :src="item.avatar" />
                      <span class="member">{{ item.name }}</span>
                    </a>
                  </a-col>
                </a-row>
              </div>
            </a-spin>
          </div>
        </a-card>
      </a-col>
      <a-col :md="24" :lg="17">
        <a-card
          style="width:100%"
          :bordered="false"
          :tab-list="tabListNoTitle"
          :active-tab-key="noTitleKey"
          @tabChange="key => handleTabChange(key, 'noTitleKey')"
        >
          <article-page v-if="noTitleKey === 'article'" />
          <app-page v-else-if="noTitleKey === 'app'" />
          <project-page v-else-if="noTitleKey === 'project'" />
        </a-card>
      </a-col>
    </a-row>
  </div>
</template>

<script lang="ts">
import { Component, Vue } from 'vue-property-decorator'
import { Getter } from 'vuex-class'
import { PageView, RouteView } from '@/layouts'
import { AppPage, ArticlePage, ProjectPage } from './page'

@Component({
  name: 'CenterIndex',
  components: {
    RouteView,
    PageView,
    AppPage,
    ArticlePage,
    ProjectPage
  }
})
export default class CenterIndex extends Vue {
  // data
  private tags: string[] = ['很有想法的', '专注设计', '辣~', '大长腿', '川妹子', '海纳百川']
  private tagInputVisible: boolean = false
  private tagInputValue: string = ''
  private teams: any[] = []
  private teamSpinning: boolean = true
  private tabListNoTitle: any[] = [
    {
      key: 'article',
      tab: '文章(8)'
    },
    {
      key: 'app',
      tab: '应用(8)'
    },
    {
      key: 'project',
      tab: '项目(8)'
    }
  ]
  private noTitleKey: string = 'app'

  constructor() {
    super()
  }

  mounted() {
    this.getTeams()
  }

  // methods
  //...mapGetters(['nickname', 'avatar']),
  @Getter('nickname', { namespace: 'user' })
  private nickname!: string
  @Getter('avatar', { namespace: 'user' })
  private avatar!: string

  getTeams() {
    this.$http.get('/workplace/teams').then((res: any) => {
      this.teams = res.result
      this.teamSpinning = false
    })
  }

  handleTabChange(key: any, type: any) {
    ;(this as any)[type] = key
  }

  handleTagClose(removeTag: string) {
    const tags = this.tags.filter(tag => tag !== removeTag)
    this.tags = tags
  }

  showTagInput() {
    this.tagInputVisible = true
    this.$nextTick(() => {
      ;(this.$refs.tagInput as any).focus()
    })
  }

  handleInputChange(e: any) {
    this.tagInputValue = e.target.value
  }

  handleTagInputConfirm() {
    const inputValue = this.tagInputValue
    let tags = this.tags
    if (inputValue && !tags.includes(inputValue)) {
      tags = [...tags, inputValue]
    }

    Object.assign(this, {
      tags,
      tagInputVisible: false,
      tagInputValue: ''
    })
  }
}
</script>

